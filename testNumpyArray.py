import numpy as np 
a = np.array(
   [[11,12,13,14,15],
    [21,22,23,24,25],
    [31,32,33,34,35],
    [41,42,43,44,45],
    [51,52,53,54,55]]
)

# >>>
"""
   [[11,12,13,14,15],
    [21,22,23,24,25],
    [31,32,33,34,35],
    [41,42,43,44,45],
    [51,52,53,54,55]] 
"""
print(a)

print(a[0][0]) # >>> 11

print(a[0,0]) # >>> 11

print(a[0,0:5]) # >>> [11 12 13 14 15]

print(a[1:4,0]) # >>> [21 31 41]

print(a[:,2]) # >>> [13 23 33 43 53]

print(a[1:4,1:4]) # >>>
'''
   [[22 23 24]
    [32 33 34]
    [42 43 44]]
'''
